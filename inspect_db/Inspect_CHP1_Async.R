BB <- opendata()

## get all async steps
stepdays <- data.table(
    BB |>
        filter(!is.na(Async_step_count), Elevat > 0) |>
        select(Date, Async_step_count)  |>
        collect()
)
steplim <- 1000

plot(stepdays)
abline(h = steplim)


stepdays <- stepdays[ Async_step_count > steplim]

daystodo <- unique(as.Date(stepdays$Date))

for (ad in daystodo) {
    ad     <- as.Date(ad, origin = "1970-01-01")
    yearsq <- unique(year(ad))
    monthq <- unique(month(ad))
    gather <- data.table(open_dataset(sources       = DB_DIR,
                                      hive_style    = FALSE,
                                      partitioning  = c("year", "month")) |>
                             filter(year %in% yearsq & month %in% monthq) |>
                             filter(as.Date(Date) %in% ad ) |>
                             collect())

    gather[!is.na(Async_step_count), Async_step_count, Date]
    ## steps are not minutes

}




